---- MAIN INIT ----

function luaInit(this)
	
	Mission = this
	Mission.ScriptFile = "Scripts/missions/IJN/ijn_sandbox.lua"

	this.Name = "AxisSandBox"
	this.HelperLog = false
	this.CustomLog = true
	this.DamageLog = false

	---- PARTY SETTING ----
	
	this.Party = SetParty(this, PARTY_ALLIED)
	
	---- DIFFICULTY SETTING ----
	
	Mission.Difficulty = GetDifficulty()
	
	if Mission.Difficulty == 0 then
		Mission.SkillLevel = SKILL_STUN
	elseif Mission.Difficulty == 1 then
		Mission.SkillLevel = SKILL_SPNORMAL
	elseif Mission.Difficulty == 2 then
		Mission.SkillLevel = SKILL_ELITE
	end
	if Mission.Difficulty == 0 then
		Mission.SkillLevelOwn = SKILL_ELITE
	elseif Mission.Difficulty == 1 then
		Mission.SkillLevelOwn = SKILL_SPVETERAN
	elseif Mission.Difficulty == 2 then
		Mission.SkillLevelOwn = SKILL_SPNORMAL
	end
	
	---- SCORING ----
	
	Scoring_SetFinalScoringFunctionName("luaObj_DoScoring")
	luaObj_FillSingleScores()
	
	---- INIT FUNCT. ----
	
	Blackout(false, "", 1)
	
	local trg1 = FindEntity("MovieShip")
	
	local dist = 250
	
	if trg1.Class.Type == "BattleShip" or trg1.Class.Type == "MotherShip" then
	
		dist = 325
	
	elseif trg1.Class.Type == "Destroyer" or trg1.Class.Type == "Submarine" then
		
		dist = 167
		
	elseif trg1.Class.Type == "LandingShip" then
		
		dist = 125
		
	elseif trg1.Class.Type == "TorpedoBoat" then
		
		dist = 50
		
		
	end
	
	luaIngameMovie(
	{
	   
	  {["postype"] = "cameraandtarget", ["target"] = trg1, ["distance"] = dist, ["theta"] = 11, ["rho"] = -30, ["moveTime"] = 0},
	  {["postype"] = "cameraandtarget", ["target"] = trg1, ["distance"] = dist, ["theta"] = 11, ["rho"] = -30, ["moveTime"] = 70},
	   
	}, luaStop, true)
	
	---- MUSIC SETTING ----
	
	Music_Control_SetLevel(MUSIC_CALM)
	luaCheckMusicSetMinLevel(MUSIC_CALM)
	
end

